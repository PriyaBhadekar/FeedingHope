<b>First Unit -  FeedingHope</b>
<p class="sub-title">Every day, tons of perfectly edible food are discarded by businesses, events, and households, contributing to both environmental degradation and the perpetuation of food insecurity. FeedingHope steps in to intercept this surplus food before it goes to waste, redirecting it to those who need it most.</p>
<p class="sub-title">Through our user-friendly website, donors can easily list their excess food items, whether it's leftover from events, restaurants, or personal gatherings. Our network of partner organizations, including shelters, food banks, and community centers, then efficiently coordinate the pickup and distribution of these donations to ensure they reach individuals and families facing hunger.</p>
<p class="sub-title">But our mission doesn't stop there. FeedingHope is committed to fostering sustainable change by addressing the root causes of food waste and hunger. In addition to facilitating food donations, we also empower communities through educational initiatives, advocacy efforts, and innovative projects aimed at reducing food waste and promoting food security.</p>
<p class="sub-title">Our fundraising section plays a crucial role in sustaining our operations and expanding our impact. By donating to FeedingHope, supporters contribute to our ongoing efforts to build a more equitable and resilient food system, where no one goes to bed hungry and where surplus food becomes a valuable resource for those in need.</p>
